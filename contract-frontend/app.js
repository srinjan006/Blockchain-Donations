let web3;
let contract;

window.onload = async () => {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        await window.ethereum.request({ method: "eth_requestAccounts" });
        contract = new web3.eth.Contract(contractABI, contractAddress);
        updateUI();
    } else {
        alert("Please install MetaMask!");
    }
};

async function updateUI() {
    const balance = await contract.methods.contractBalance().call();
    document.getElementById("totalDonations").innerText = web3.utils.fromWei(balance, "ether");
}

async function donate() {
    const amount = document.getElementById("donationAmount").value;
    const accounts = await web3.eth.getAccounts();
    await contract.methods.donate().send({ from: accounts[0], value: web3.utils.toWei(amount, "ether") });
    updateUI();
}

async function withdrawFunds() {
    const accounts = await web3.eth.getAccounts();
    await contract.methods.withdrawFunds(web3.utils.toWei("0.1", "ether")).send({ from: accounts[0] });
    updateUI();
}
